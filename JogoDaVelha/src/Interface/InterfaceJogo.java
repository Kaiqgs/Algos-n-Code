    public void reiniciaJogo()
    {
        //Desabilita botão de restart
        restartBotao.setEnabled(false);
        //Reabilita todos botões
        defineEstadoBotoes(true);

        //Re-declara motor, com pontuacao nova
        motor = new MotorJogoDaVelha(motor.getPontuacaoPlayers());

        //Atualiza interface
        renderizaBotoes();
    }

    public void checaFimDeJogo()
    {
        //Caso fim de jogo
        if (motor.fimDeJogo())
        {
            encerraJogo();
        }
    }

    public void defineEstadoBotoes(boolean estado)
    {
        //Para cada botão, muda o estado
        for (JButton aux : botoesJogo)
        {
            aux.setEnabled(estado);
        }

    }

    public void encerraJogo()
    {
        //Desabilita botoes
        defineEstadoBotoes(false);
        //Atualiza texto de turno
        renderizaTurno(true);
        //Atualiza pontos
        motor.atualizaPontuacao();
        //Atuliza pontos interface
        renderizaPontuacao();
        //Ativa botão restart
        restartBotao.setEnabled(true);
    }

    public void renderizaTurno(boolean fimDeJogo)
    {
        //Altera string turno
        
        String turnoTexto;
        
        //Caso seja fim de jogo;
        if(fimDeJogo)turnoTexto = "\"" + motor.getGanhador() + "\" foi ganhador!";
        else turnoTexto = "Quem joga é o player: \"" + motor.getPlayerTurno() + "\"";
        
        turnoLabel.setText(turnoTexto);

    }

    public void renderizaPontuacao()
    {
        //Pega players do motor
        String ps[] = motor.getPlayers();
        //Pega pontuacao players
        int[] pontPlayers = motor.getPontuacaoPlayers();

        //Atualiza textos
        String t1 = ps[0] + ": " + pontPlayers[0];
        placarP1Label.setText(t1);

        String t2 = ps[1] + ": " + pontPlayers[1];
        placarP2Label.setText(t2);
    }

    public void renderizaBotoes()
    {
        //Pega tabuleiro do motor
        String[] tab = motor.getTabuleiro();

        //Pra cada botãoo
        for (int i = 0; i < tab.length; ++i)
        {
            //Se respectivo botão esta ativado
            if (botoesJogo[i].isEnabled())
            {
                //Muda texto para o mesmo do tabuleiro
                botoesJogo[i].setText(tab[i]);

                //Se respectiva celula do tabuleiro não for vazio
                if (!tab[i].equals(MotorJogoDaVelha.charVazio))
                {
                    //Desative o botão respectivo
                    botoesJogo[i].setEnabled(false);
                }
            }
        }
        //Renderiza turno (não final de jogo)
        renderizaTurno(false);
    }

