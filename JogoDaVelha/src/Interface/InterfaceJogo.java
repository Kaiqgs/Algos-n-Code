    private void botaoFazJogada(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botaoFazJogada
    {//GEN-HEADEREND:event_botaoFazJogada
        int i;//Loop pra identificar o index do botão apertado;
        for (i = 0; i < botoesJogo.length; ++i)
        {
            if (evt.getSource().equals(botoesJogo[i]))
            {
                break;
            }
        }

        //Atualizar tabuleiro de acordo com o index e o turno;
        motor.atualizaTabuleiro(i, motor.getPlayerTurno());

        //Atualiza interface
        renderizaBotoes();
        //Teste se jogo terminou ou não
        checaFimDeJogo();


    }//GEN-LAST:event_botaoFazJogada

